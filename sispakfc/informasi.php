<p>Optik berasal dari bahasa Latin “óπ” yang memiliki arti yaitu tampilan. Optik adalah cabang ilmu fisika yang membahas perilaku dan sifat cahaya, dan juga interaksi cahaya dengan materi. Definisi optik / op.tik / menurut Kamus Besar Bahasa Indonesia (KBBI) yaitu berhubungan dengan penglihatan (cahaya, lensa mata, dan sebagainya).</p>
